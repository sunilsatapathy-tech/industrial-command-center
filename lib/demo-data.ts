export function buildSummary(
  equipment: Equipment[],
  alarms: Alarm[],
  maintenanceRecords: MaintenanceRecord[],
  inventoryItems: InventoryItem[]
): DashboardSummary {
  const runningEquipment = equipment.filter((item) => item.status === 'Running').length;
  const activeAlarms = alarms.filter((item) => item.status !== 'resolved').length;
  const openWorkOrders = maintenanceRecords.filter((item) => item.status !== 'Completed').length;
  const averageOee =
    equipment.reduce((total, item) => total + item.oee, 0) / Math.max(equipment.length, 1);
  const totalInventoryUnits = inventoryItems.reduce((total, item) => total + item.quantity, 0);

  return {
    total_equipment: equipment.length,
    running_equipment: runningEquipment,
    active_alarms: activeAlarms,
    open_work_orders: openWorkOrders,
    average_oee: Number(averageOee.toFixed(1)),
    total_inventory_units: totalInventoryUnits,
  };
}

